


package IOE_Students.business.domain;


public class Institution {
    private String InstitutionName;
    private String InstitutionEmail;
    private String InstitutionPhone;
    private String InstitutionWebsite;
    private String InstitutionAddressStreet;
    private String InstitutionNote;

    public String getInstitutionname() {
        return InstitutionName;
    }

    public void setInstitutionname(String InstitutionName) {
        this.InstitutionName = InstitutionName;
    }

    public String getInstitutionemail() {
        return InstitutionEmail;
    }

    public void setInstitutionemail(String InstitutionEmail) {
        this.InstitutionEmail = InstitutionEmail;
    }

    public String getInstitutionphone() {
        return InstitutionPhone;
    }

    public void setInstitutionphone(String InstitutionPhone) {
        this.InstitutionPhone = InstitutionPhone;
    }

    public String getInstitutionwebsite() {
        return InstitutionWebsite;
    }

    public void setInstitutionwebsite(String InstitutionWebsite) {
        this.InstitutionWebsite = InstitutionWebsite;
    }

    public String getInstitutionaddressstreet() {
        return InstitutionAddressStreet;
    }

    public void setInstitutionaddressstreet(String InstitutionAddressStreet) {
        this.InstitutionAddressStreet = InstitutionAddressStreet;
    }

    public String getInstitutionnote() {
        return InstitutionNote;
    }

    public void setInstitutionnote(String InstitutionNote) {
        this.InstitutionNote = InstitutionNote;
    }
}
